
public class Strings {

	public static void main(String[] args) {
		
		//8 primitive types, eg int
		int lama = 2;
		
		//String with uppercase, it's a class
		String dupa = "Dupa";
		String wita = "wita";
		String stwosza = "Stwosza";
		String spacja = " ";
		
		String calosc = dupa + spacja + wita + spacja + stwosza; 
		
		System.out.println(dupa + spacja + wita + spacja + stwosza);
		System.out.println(calosc);
		System.out.println("Dupa"+" "+"jasia");
		System.out.println(calosc + "*" + lama);
		
	}
	
}
